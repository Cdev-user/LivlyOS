/*
====================
AUFBAU VON IDT
====================

Bits 0–15    : Adresse des Handlers, untere 16 Bit
Bits 16–31   : Code-Segment-Selektor (= 0x08, dein Kernel-Code-Segment)
Bits 32–39   : IST (Interrupt Stack Table) — für den Anfang 0
Bits 40–47   : Type + Flags (Interrupt Gate = 0x8E)
Bits 48–63   : Adresse des Handlers, mittlere 16 Bit
Bits 64–95   : Adresse des Handlers, obere 32 Bit
Bits 96–127  : reserviert, immer 0

idt.h
  └─ IDT-Eintrag Struct (16 Bytes, packed)
  └─ IDTR Struct (Zeiger + Größe für LIDT-Instruktion)
  └─ idt_init() Deklaration
  └─ Exception-Namen als String-Array (für Fehlermeldungen)

idt.c
  └─ IDT-Tabelle: 256 Einträge
  └─ idt_set_entry() — einen Eintrag setzen
  └─ idt_init() — alle 256 Einträge füllen, LIDT ausführen
  └─ exception_handler() — gibt Fehlermeldung aus, hält an
  └─ ASM-Stubs für jeden Handler (die CPU übergibt einen Error Code
     nur bei bestimmten Exceptions — die Stubs gleichen das an)

    handler_low
    selector
    ist
    flags
    handler_mid
    handler_high
    reserved

*/

typedef struct {
    uint16_t handler_low;
    uint16_t selector;
    uint8_t IST;
    uint8_t flags;
    uint16_t handler_mid;
    uint32_t handler_high;
    uint32_t reserved;
}__attribute__((packed)) IDTEntry;

typedef struct {
    uint16_t limit;
    uint64_t base;
}__attribute__((packed)) IDTR;

typedef struct{
    uint64_t r15, r14, r13, r12, r11, r10, r9, r8;
    uint64_t rbp, rdi, rsi, rdx, rcx, rbx, rax;
    uint64_t int_num;
    uint64_t error_code;
    uint64_t rip;
    uint64_t cs;
    uint64_t rflags;
    uint64_t rsp;
    uint64_t ss;
}__attribute__((packed)) InterruptFrame;


